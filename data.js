/* Colebrook-White friction factor (iterative), turbulent; Re, relative roughness eD */
VP.frictionFactor = function(Re, eD){
  if(Re < 2300) return 64/Re;            // laminar
  let f = 0.02;
  for(let i=0;i<60;i++){
    const rhs = -2*Math.log10(eD/3.7 + 2.51/(Re*Math.sqrt(f)));
    const fn = 1/(rhs*rhs);
    if(Math.abs(fn-f)<1e-8){f=fn;break;}
    f=fn;
  }
  return f;
};

