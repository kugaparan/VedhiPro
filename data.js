/* generic linear interpolation lookup in a 2D array; col x is key, returns row interpolated */
VP.interpRow = function(table, xcol, xval){
  if(xval <= table[0][xcol]) return table[0];
  if(xval >= table[table.length-1][xcol]) return table[table.length-1];
  for(let i=0;i<table.length-1;i++){
    const a=table[i], b=table[i+1];
    if(xval>=a[xcol] && xval<=b[xcol]){
      const f=(xval-a[xcol])/(b[xcol]-a[xcol]);
      return a.map((v,j)=>v + f*(b[j]-v));
    }
  }
  return table[table.length-1];
};

VP.satByTemp = function(T){ return VP.interpRow(VP.steamSat,0,T); };
VP.satByPress = function(P_kPa){ return VP.interpRow(VP.steamSat,1,P_kPa); };

/* Saturation pressure of water (kPa) — Antoine / Arden-Buck for psychrometrics, T in °C */
VP.pSatWater = function(T){
  // Arden Buck equation, returns kPa
  return 0.61121 * Math.exp((18.678 - T/234.5) * (T/(257.14+T)));
};

